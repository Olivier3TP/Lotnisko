public class Rezerwacja
{
    private String imie;
    private String nazwisko;
    private int cenaBiletu;
    private int numerLotu;

    public Rezerwacja(String imie, String nazwisko, int cenaBiletu, int numerLotu) {
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.cenaBiletu = cenaBiletu;
        this.numerLotu = numerLotu;
    }
}
